SELECT *
FROM EMPLOYEE;

SELECT
  COUNT(CODE),
  CODE
FROM EMPLOYEE
GROUP BY CODE
HAVING COUNT(CODE) > 1;

SELECT
  count(1),
  DEPARTMENT
FROM EMPLOYEE
GROUP BY DEPARTMENT;

SELECT
  MAX(salary) AS MAX_SALARY,
  DEPARTMENT
FROM EMPLOYEE
GROUP BY DEPARTMENT
HAVING DEPARTMENT IN ('DEV', 'QA');

SELECT
  SUM(SALARY) AS SUM_SALARY,
  DEPARTMENT
FROM EMPLOYEE
GROUP BY DEPARTMENT
ORDER BY SUM(SALARY) DESC;


